			<h2 id="topic"><b>Causes of Cancer</b></h2>
			<p class="text text-justify">The majority of cancers, some 90–95% of cases, are due to environmental factors. The remaining 5–10% are due to inherited genetics. Environmental, as used by cancer researchers, means any cause that is not inherited genetically, such as lifestyle, economic and behavioral factors and not merely pollution. Common environmental factors that contribute to cancer death include tobacco (25–30%), diet and obesity (30–35%), infections (15–20%), radiation (both ionizing and non-ionizing, up to 10%), stress, lack of physical activity and pollution.</p>
			<p class="text text-justify">It is not generally possible to prove what caused a particular cancer because the various causes do not have specific fingerprints. For example, if a person who uses tobacco heavily develops lung cancer, then it was probably caused by the tobacco use, but since everyone has a small chance of developing lung cancer as a result of air pollution or radiation, the cancer may have developed for one of those reasons. Excepting the rare transmissions that occur with pregnancies and occasional organ donors, cancer is generally not a transmissible disease.</p>
			<h3><b>Chemicals</b></h3>
			<p class="text text-justify">Exposure to particular substances have been linked to specific types of cancer. These substances are called carcinogens.Tobacco smoke, for example, causes 90% of lung cancer. It also causes cancer in the larynx, head, neck, stomach, bladder, kidney, esophagus and pancreas. Tobacco smoke contains over fifty known carcinogens, including nitrosamines and polycyclic aromatic hydrocarbons.</p>
		</div>
		<h3><b>Diet and Exercise</b></h3>
		<p class="text text-justify">Diet, physical inactivity and obesity are related to up to 30–35% of cancer deaths. In the United States excess body weight is associated with the development of many types of cancer and is a factor in 14–20% of cancer deaths. A UK study including data on over 5 million people showed higher body mass index to be related to at least 10 types of cancer and responsible for around 12,000 cases each year in that country. Physical inactivity is believed to contribute to cancer risk, not only through its effect on body weight but also through negative effects on the immune system and endocrine system. More than half of the effect from diet is due to overnutrition (eating too much), rather than from eating too few vegetables or other healthful foods.</p>
		<h3><b>Infection</b></h3>
		<p class="text text-justify">Worldwide approximately 18% of cancer deaths are related to infectious diseases. This proportion ranges from a high of 25% in Africa to less than 10% in the developed world. Viruses (Oncoviruses) are the usual infectious agents that cause cancer but cancer bacteria and parasites may also play a role.</p>
		<h3><b>Autoimmune Diseases</b></h3>
		<p class="text text-justify">There is an association between celiac disease and an increased risk of all cancers. People with untreated celiac disease have a higher risk, but this risk decreases with time after diagnosis and strict treatment, probably due to the adoption of a gluten-free diet, which seems to have a protective role against development of malignancy in people with celiac disease. However, the delay in diagnosis and initiation of a gluten-free diet seems to increase the risk of malignancies. Rates of gastrointestinal cancers are increased in people with Crohn's disease and ulcerative colitis, due to chronic inflammation. Also, immunomodulators and biologic agents used to treat these diseases may promote developing extra-intestinal malignancies.</p>
		<h3><b>Radiation</b></h3>
		<p class="text text-justify">Up to 10% of invasive cancers are related to radiation exposure, including both ionizing radiation and non-ionizing ultraviolet radiation. Additionally, the majority of non-invasive cancers are non-melanoma skin cancers caused by non-ionizing ultraviolet radiation, mostly from sunlight. Sources of ionizing radiation include medical imaging and radon gas.</p>
